#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*dest;

	dest = (char*)s;
	i = 0;
	while (n > 0)
	{
		dest[i] = '\0';
		i++;
		n--;
	}
	s = dest;
}
